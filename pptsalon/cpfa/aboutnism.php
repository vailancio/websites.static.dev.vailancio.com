                <h1>NISM aims to lead, catalyze and deliver educational initiatives to enhance the quality of securities markets</h1>

<p>The Certified Personal Financial Advisor (CPFA) examination is designed and conducted by NISM.</p>
<p>National Institute of Securities Markets (NISM) is a public trust established by the Securities and Exchange Board of India (SEBI), the regulator for securities markets in India. </p>
<p>NISM seeks to add to market quality through educational initiatives. It is an autonomous body governed by a Board of Governors and with strategic guidance from an international Advisory Council. NISM’s most important mission is to build capacity among and for the stakeholders in the securities markets through financial literacy, professional education, enhancing governance standards and  fostering policy research.</p>
<strong>School for Certification of Intermediaries</strong>
<p>The School for Certification of Intermediaries (SCI) at NISM is engaged in developing certification examinations for professionals employed in various segments of the Indian securities markets. Most of these examinations are being developed by NISM as mandated under SEBI (Certification of Associated Persons in the Securities Markets) Regulation, 2007</p>

<h1>Financial Planning Corporation (India) Pvt. Ltd. (FPCIL) is a knowledge partner of NISM for the CPFA Examination.</h1> 
<p>The certificate awarded to candidates who pass the CPFA examination will be jointly signed by NISM and FPCIL. FPCIL was established by Financial Planning Standards Board India (FPSB India) along with BNP Paribas SA, SBI, and Tata AIG Life Insurance Co. Ltd, essentially to undertake education activities in the Financial Planning segment.</p>
